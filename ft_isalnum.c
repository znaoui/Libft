#include "libft.h"

int	ft_isalnum(int ly)
{
	if ((ly >= 'a' && ly <= 'z') || (ly >= 'A' && ly <= 'Z')
		|| (ly >= '0' && ly <= '9'))
		return (1);
	else
		return (0);
}
