#include "libft.h"

void	ft_bzero(void *src, size_t size)
{
	unsigned char	*dest;
	size_t			i;

	i = 0;
	dest = src;
	while (i < size)
	{
		*dest = 0;
		dest++;
		i++;
	}
}
